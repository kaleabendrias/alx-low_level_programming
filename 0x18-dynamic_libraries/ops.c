/**
 * add - add
 * @a: first
 * @b: second
 * Return: int
 */

int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - add
 * @a: first
 * @b: second
 * Return: int
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - add
 * @a: first
 * @b: second
 * Return: int
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - add
 * @a: first
 * @b: second
 * Return: int
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - add
 * @a: first
 * @b: second
 * Return: int
 */
int mod(int a, int b)
{
	return (a % b);
}
